    private void dibujaElPokemonQueEstaEnLaPosicion (int posicion){
        int fila = posicion / 31;
        int columna = posicion % 31;
        Graphics2D g2 = (Graphics2D) buffer.getGraphics();
        //borro lo que hubiera
        g2.setColor(Color.YELLOW);
        g2.fillRect(0, 0, alto, ancho);
        g2.drawImage(imagenPokemons,
                20,
                20,
                ancho,
                alto,
                96*columna,
                96*fila,
                96*columna + 96,
                96*fila + 96,
                null);
        repaint();
        escribeDatos();
    }
    
    private void escribeDatos(){
        Pokemon p = listaPokemons.get(String.valueOf(contador+1));
        if (p != null){
            jLabel2.setText("Nombre: " + p.nombre);                                          //Representa en texto la base de datos en la columna nombre en el jLabel
            jLabel3.setText(String.valueOf("Generacion: " + p.generation_id));                   //Representa en texto la base de datos en la columna generation_id en el jLabel
            jLabel4.setText(String.valueOf("Evolucion: " + p.evolution_chain_id));              //Representa en texto la base de datos en la columna evolution_chain_id en el jLabel  
            jLabel5.setText("Especie: " + p.especies);
            jLabel6.setText("Color: " + p.color);
            jLabel7.setText("Alto: " + p.alto);
            jLabel8.setText("Ancho: " + p.ancho);
        }
        else {
            jLabel2.setText("NO HAY DATOS");
        }
    }
